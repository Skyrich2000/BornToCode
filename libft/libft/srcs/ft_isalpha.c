int	ft_isalpha(int c)
{
	unsigned char	chr;

	chr = (unsigned char)c;
	if (c > 255)
		return (0);
	if (('a' <= chr && chr <= 'z') || ('A' <= chr && chr <= 'Z'))
		return (1);
	return (0);
}
