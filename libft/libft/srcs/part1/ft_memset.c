#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	void	*ret;
	char	*ptr;

	ret = s;
	ptr = (char *)s;
	while (n--)
	{
		*ptr = c;
		++ptr;
	}
	return (ret);
}
